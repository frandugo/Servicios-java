    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        try {
            if (!txtemprenombre.getText().isEmpty() && !txtempredesc.getText().isEmpty() && !txtemprepais.getText().isEmpty() && !txtempreciudad.getText().isEmpty()) {
                EmpresaDAO dao = new EmpresaDAO();
                EmpresaBean empr = new EmpresaBean();
                empr.setEmprnomb(txtemprenombre.getText());
                empr.setEmprdesc(txtempredesc.getText());
                empr.setEmprpais(txtemprepais.getText());
                empr.setEmprciud(txtempreciudad.getText());
                dao.insertEmpresa(empr);
                //Carga de datos de empresas
                loadTablaEmpresa();
                //Se limpian los campos
                limpiarCampos();
                //Mensaje
                JOptionPane.showMessageDialog(null, "La empresa fue registrada");
            } else {
                JOptionPane.showMessageDialog(null, "Los siguientes campos son obligatorios:\n\n"
                        + "* Nombre\n"
                        + "* Descripcion\n"
                        + "* Pais\n"
                        + "* Ciudad\n");
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminEmpresa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnagregarActionPerformed

    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        try {
            if (!txtempreid.getText().isEmpty()) {
                EmpresaDAO dao = new EmpresaDAO();
                EmpresaBean empr = dao.getEmpresaById(Integer.parseInt(txtempreid.getText()));
                if (empr != null) {
                    txtemprenombre.setText(empr.getEmprnomb());
                    txtempredesc.setText(empr.getEmprdesc());
                    txtemprepais.setText(empr.getEmprpais());
                    txtempreciudad.setText(empr.getEmprciud());
                    setEmprid(empr.getEmprid());
                } else {
                    limpiarCampos();
                    JOptionPane.showMessageDialog(null, "No se encontraron datos relacionados al Id");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Los siguientes campos son obligatorios:\n\n"
                        + "* Buscar por Id\n");
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminEmpresa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        try {
            if (getEmprid() > 0 && !txtempreid.getText().isEmpty() && !txtemprenombre.getText().isEmpty() && !txtempredesc.getText().isEmpty() && !txtemprepais.getText().isEmpty() && !txtempreciudad.getText().isEmpty()) {
                EmpresaDAO dao = new EmpresaDAO();
                EmpresaBean empr = new EmpresaBean();
                empr.setEmprid(getEmprid());
                empr.setEmprnomb(txtemprenombre.getText());
                empr.setEmprdesc(txtempredesc.getText());
                empr.setEmprpais(txtemprepais.getText());
                empr.setEmprciud(txtempreciudad.getText());
                dao.updateEmpresa(empr);
                //Carga de datos de empresas
                loadTablaEmpresa();
                //Se limpian los campos
                limpiarCampos();
                //Mensaje
                JOptionPane.showMessageDialog(null, "La empresa fue actualizada");
            } else {
                JOptionPane.showMessageDialog(null, "Los siguientes campos son obligatorios:\n\n"
                        + "* Buscar por Id\n"
                        + "* Nombre\n"
                        + "* Descripcion\n"
                        + "* Pais\n"
                        + "* Ciudad\n");
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminEmpresa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnactualizarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        try {
            if (getEmprid() > 0 && !txtempreid.getText().isEmpty()) {
                EmpresaDAO dao = new EmpresaDAO();
                dao.deleteEmpresa(getEmprid());
                //Carga de datos de empresas
                loadTablaEmpresa();
                //Se limpian los campos
                limpiarCampos();
                //Mensaje
                JOptionPane.showMessageDialog(null, "La empresa fue eliminada");
            } else {
                JOptionPane.showMessageDialog(null, "Los siguientes campos son obligatorios:\n\n"
                        + "* Buscar por Id\n");
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminEmpresa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btneliminarActionPerformed

