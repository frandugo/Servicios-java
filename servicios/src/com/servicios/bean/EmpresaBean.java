package com.servicios.bean;

public class EmpresaBean {

    private int emprid;
    private String emprnomb;
    private String emprdesc;
    private String emprciud;
    private String emprpais;
    private String emprestd;

    public EmpresaBean() {
    }

    /**
     * @return the emprid
     */
    public int getEmprid() {
        return emprid;
    }

    /**
     * @param emprid the emprid to set
     */
    public void setEmprid(int emprid) {
        this.emprid = emprid;
    }

    /**
     * @return the emprnomb
     */
    public String getEmprnomb() {
        return emprnomb;
    }

    /**
     * @param emprnomb the emprnomb to set
     */
    public void setEmprnomb(String emprnomb) {
        this.emprnomb = emprnomb;
    }

    /**
     * @return the emprdesc
     */
    public String getEmprdesc() {
        return emprdesc;
    }

    /**
     * @param emprdesc the emprdesc to set
     */
    public void setEmprdesc(String emprdesc) {
        this.emprdesc = emprdesc;
    }

    /**
     * @return the emprciud
     */
    public String getEmprciud() {
        return emprciud;
    }

    /**
     * @param emprciud the emprciud to set
     */
    public void setEmprciud(String emprciud) {
        this.emprciud = emprciud;
    }

    /**
     * @return the emprpais
     */
    public String getEmprpais() {
        return emprpais;
    }

    /**
     * @param emprpais the emprpais to set
     */
    public void setEmprpais(String emprpais) {
        this.emprpais = emprpais;
    }

    /**
     * @return the emprestd
     */
    public String getEmprestd() {
        return emprestd;
    }

    /**
     * @param emprestd the emprestd to set
     */
    public void setEmprestd(String emprestd) {
        this.emprestd = emprestd;
    }
}
