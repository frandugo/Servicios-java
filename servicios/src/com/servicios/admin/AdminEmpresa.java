    public void loadTablaEmpresa() {
        try {
            EmpresaDAO dao = new EmpresaDAO();
            ArrayList<EmpresaBean> empresas = dao.getEmpresas();
            String col[] = {"Id", "Nombre", "Descripcion", "Pais", "Ciudad"};
            DefaultTableModel defaultTableModel = new DefaultTableModel(col, 0);
            for (EmpresaBean empr : empresas) {
                Object[] data = {empr.getEmprid(), empr.getEmprnomb(), empr.getEmprdesc(), empr.getEmprpais(), empr.getEmprciud()};
                defaultTableModel.addRow(data);
            }
            tblempresas.setModel(defaultTableModel);
        } catch (SQLException ex) {
            Logger.getLogger(AdminEmpresa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

